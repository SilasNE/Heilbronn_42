#include <unistd.h>

int ft_str_is_alpha(char *str)
{
    while (*str)
    {
        if (!((*str >= 'A' && *str <= 'Z') || (*str >= 'a' && *str <= 'z')))
        {
            write(1, "0\n", 2);
            return (0);
        }
        str++;
    }
    write(1, "1\n", 2);
    return (1);
}
