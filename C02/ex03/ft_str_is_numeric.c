#include <unistd.h>

int ft_str_is_numeric(char *str)
{
    while (*str)
    {
        if (!(*str >= '0' && *str <= '9'))
        {
            write(1, "0\n", 2);
            return (0);
        }
        str++;
    }
    write(1, "1\n", 2);
    return (1);
}
